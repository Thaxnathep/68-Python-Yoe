num = 1                      # กำหนดตัวเลขเริ่มต้นที่ 1
total_max = 100              # กำหนดตัวเลขสูงสุดที่ต้องการแสดง (100)

rows = int(input("input your row: "))  # รับค่าจำนวนแถวจากผู้ใช้

for r in range(rows):                  # วนลูปตามจำนวนแถวที่ผู้ใช้กำหนด
    cols = (total_max // rows)         # คำนวณจำนวนคอลัมน์ต่อแถว โดยใช้การหารเอาส่วนเฉพาะจำนวนเต็ม
    
    for c in range(cols):              # วนลูปตามจำนวนคอลัมน์ที่คำนวณได้
        if num <= total_max:           # ตรวจสอบว่ายังไม่เกินเลขสูงสุด (100)
            print(f"{num:3}", end=" ") # แสดงเลข โดยจัดระยะให้กว้าง 3 ช่อง และไม่ขึ้นบรรทัดใหม่
            num += 1                   # เพิ่มค่าเลขทีละ 1
        else:
            print("  ", end=" ")       # ถ้าเลขหมดแล้วให้แสดงเป็นช่องว่างแทน
    
    print()  # <-- ขึ้นบรรทัดใหม่หลังจากแสดงครบทุกคอลัมน์ในแถว