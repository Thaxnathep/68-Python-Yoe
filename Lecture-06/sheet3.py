# สร้างเมทริกซ์ 3x3
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# เปลี่ยนค่าของสมาชิกแถวที่ 1 (index = 0) คอลัมน์ที่ 2 (index = 1) ให้เป็น 10
matrix[0][1] = 10

# แสดงผลเมทริกซ์แบบปกติ (จะเห็นเป็น list ซ้อน list)
print("เมทริกซ์หลังแก้ไข:", matrix)

print("\n=== แสดงผลเมทริกซ์แบบตาราง ===")
# วนลูปแต่ละแถว
for row in matrix:
    # วนลูปแต่ละค่าในแถว
    for element in row:
        # แสดงผลแบบจัดระยะ 4 ช่องให้สวยงาม
        print(f"{element:4}", end=" ")
    print()  # ขึ้นบรรทัดใหม่เมื่อจบแต่ละแถว